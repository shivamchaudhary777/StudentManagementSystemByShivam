package com.example.studentmanagement;

public class StudentItem {

        private String name;
        private int rollNumber;

        public StudentItem( String name,  int rollNumber) {

          this.name=name;
          this.rollNumber=rollNumber;
        }


        public String getName() {
            return name;
        }

        public int getRollNumber() {
            return rollNumber;

        }

















}
